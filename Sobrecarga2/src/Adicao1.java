public class Adicao1 {
    public static int add(int a, int b){
        int soma = a + b;
        return soma;
    }

    public static float add(float c, float d){
        float soma = c + d;
        return soma;
    }

    public String exibe(int a, int b, int c){
        return "Soma: "+a+"+"+b+" = "+c;
    }

    public String exibe(float a, float b, float c){
        return "Soma: "+a+"+"+b+" = "+c;
    }
}
