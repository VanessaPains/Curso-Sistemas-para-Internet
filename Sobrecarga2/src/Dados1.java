public class Dados1 {
    //variaveis/atributos
    private int a, b;
    private Float c, d;

    //construtres
    public Dados1(int a, int b) {

        this.a = a;
        this.b = b;
    }

    public Dados1(Float c, Float d) {

        this.c = c;
        this.d = d;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public Float getC() {
        return c;
    }

    public void setC(Float c) {
        this.c = c;
    }

    public Float getD() {
        return d;
    }

    public void setD(Float d) {
        this.d = d;
    }

    
}
